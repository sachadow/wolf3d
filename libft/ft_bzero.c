#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			a;
	unsigned char	*ch;

	a = 0;
	ch = (unsigned char*)s;
	while (a < n)
	{
		ch[a] = 0;
		a++;
	}
}
