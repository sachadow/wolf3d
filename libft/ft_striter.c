#include "libft.h"
#include <string.h>

void	ft_striter(char *s, void (*f)(char*))
{
	int a;

	if (s == 0)
		return ;
	if (f == NULL)
		return ;
	a = 0;
	while (s[a] != '\0')
	{
		f(&(s[a]));
		a++;
	}
}
