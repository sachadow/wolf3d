char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	int		c;
	char	d;

	a = 0;
	while (str[a] != '\0')
		a++;
	a--;
	b = a / 2;
	c = 0;
	while (c < b && a > b)
	{
		d = str[a];
		str[a] = str[c];
		str[c] = d;
		a--;
		c++;
	}
	return (str);
}
