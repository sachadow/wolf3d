char	*ft_strlow(char *str)
{
	int a;

	a = 0;
	while (str[a] != 0)
	{
		if (str[a] >= 'A' && str[a] <= 'Z')
			str[a] += (97 - 65);
		a++;
	}
	return (str);
}
